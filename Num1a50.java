/**
 * Números de 1 a 50
 */
public class Num1a50 {
    public static void main(String[] args) {
       
        System.out.println("================ NÚMEROS DE 1 A 50 ================");
        
        for (int i = 1; i <= 50; i++){
            System.out.println(i);
       } 

       System.out.println("===================================================");
    }
}